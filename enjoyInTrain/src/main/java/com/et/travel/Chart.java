package com.et.travel;

public class Chart {
	private int categoryNum;
	private float count;
	private String pmCode;
	private String product;
	private int pmPrice;
	private int pmPercent;
	
	public int getCategoryNum() {
		return categoryNum;
	}
	public void setCategoryNum(int categoryNum) {
		this.categoryNum = categoryNum;
	}
	public float getCount() {
		return count;
	}
	public void setCount(float count) {
		this.count = count;
	}
	public String getPmCode() {
		return pmCode;
	}
	public void setPmCode(String pmCode) {
		this.pmCode = pmCode;
	}
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public int getPmPrice() {
		return pmPrice;
	}
	public void setPmPrice(int pmPrice) {
		this.pmPrice = pmPrice;
	}
	public int getPmPercent() {
		return pmPercent;
	}
	public void setPmPercent(int pmPercent) {
		this.pmPercent = pmPercent;
	}
	
	
}
