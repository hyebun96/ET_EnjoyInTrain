package com.et.timeTable;

public class TimeTable {
	private int ttcode;
	private String traincode;
	private String firstcode;
	private String lastcode;
	private String firsttime;
	
	private String timetableCount;
	
	
	public int getTtcode() {
		return ttcode;
	}
	public void setTtcode(int ttcode) {
		this.ttcode = ttcode;
	}
	public String getTraincode() {
		return traincode;
	}
	public void setTraincode(String traincode) {
		this.traincode = traincode;
	}
	public String getFirstcode() {
		return firstcode;
	}
	public void setFirstcode(String firstcode) {
		this.firstcode = firstcode;
	}
	public String getLastcode() {
		return lastcode;
	}
	public void setLastcode(String lastcode) {
		this.lastcode = lastcode;
	}
	public String getFirsttime() {
		return firsttime;
	}
	public void setFirsttime(String firsttime) {
		this.firsttime = firsttime;
	}
	public String getTimetableCount() {
		return timetableCount;
	}
	public void setTimetableCount(String timetableCount) {
		this.timetableCount = timetableCount;
	}
	
	
}
