package com.et.admin.trainsales;

public class TrainpersonType {
	
	private float adult;
	private float child;
	private float old;
	private float sd;
	private float md;
	
	public float getAdult() {
		return adult;
	}
	public void setAdult(float adult) {
		this.adult = adult;
	}
	public float getChild() {
		return child;
	}
	public void setChild(float child) {
		this.child = child;
	}
	public float getOld() {
		return old;
	}
	public void setOld(float old) {
		this.old = old;
	}
	public float getSd() {
		return sd;
	}
	public void setSd(float sd) {
		this.sd = sd;
	}
	public float getMd() {
		return md;
	}
	public void setMd(float md) {
		this.md = md;
	}
}
